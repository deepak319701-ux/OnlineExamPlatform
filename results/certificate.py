from reportlab.pdfgen import canvas
from reportlab.lib.colors import HexColor
from reportlab.lib.pagesizes import landscape, A4
from datetime import datetime
import random


def generate_certificate(response, student, exam, score, percentage):

    pdf = canvas.Canvas(response, pagesize=landscape(A4))

    width, height = landscape(A4)

    # ==========================
    # Background
    # ==========================

    pdf.setFillColor(HexColor("#FFFFFF"))
    pdf.rect(0, 0, width, height, fill=1)

    # ==========================
    # Double Border
    # ==========================

    pdf.setStrokeColor(HexColor("#C9A227"))
    pdf.setLineWidth(6)
    pdf.rect(20, 20, width - 40, height - 40)

    pdf.setLineWidth(2)
    pdf.rect(35, 35, width - 70, height - 70)

    # ==========================
    # Header
    # ==========================

    pdf.setFillColor(HexColor("#0B3D91"))
    pdf.setFont("Helvetica-Bold", 28)

    pdf.drawCentredString(
        width / 2,
        height - 70,
        "ONLINE EXAMINATION PLATFORM"
    )

    pdf.setFont("Helvetica-Bold", 22)

    pdf.drawCentredString(
        width / 2,
        height - 105,
        "CERTIFICATE OF ACHIEVEMENT"
    )

    # Decorative Line

    pdf.setStrokeColor(HexColor("#C9A227"))
    pdf.setLineWidth(2)

    pdf.line(
        120,
        height - 120,
        width - 120,
        height - 120
    )

    # ==========================
    # Certificate Text
    # ==========================

    pdf.setFillColor(HexColor("#444444"))
    pdf.setFont("Helvetica", 16)

    pdf.drawCentredString(
        width / 2,
        height - 170,
        "This Certificate is Proudly Presented To"
    )

    # Student Name

    pdf.setFillColor(HexColor("#0B3D91"))
    pdf.setFont("Helvetica-Bold", 30)

    pdf.drawCentredString(
        width / 2,
        height - 220,
        student.upper()
    )

    # Description

    pdf.setFillColor(HexColor("#444444"))
    pdf.setFont("Helvetica", 16)

    pdf.drawCentredString(
        width / 2,
        height - 260,
        "For Successfully Completing the Examination"
    )

    # Exam Name

    pdf.setFillColor(HexColor("#000000"))
    pdf.setFont("Helvetica-Bold", 22)

    pdf.drawCentredString(
        width / 2,
        height - 295,
        exam
    )

    # ==========================
    # Certificate Details
    # ==========================

    certificate_id = (
        f"OEP-{datetime.now().year}-{random.randint(100000,999999)}"
    )

    pdf.setFillColor(HexColor("#222222"))
    pdf.setFont("Helvetica", 14)

    pdf.drawString(
        80,
        180,
        f"Certificate ID : {certificate_id}"
    )

    pdf.drawString(
        80,
        155,
        f"Score : {score}"
    )

    pdf.drawString(
        80,
        130,
        f"Percentage : {percentage}%"
    )

    pdf.drawString(
        80,
        105,
        "Issue Date : "
        + datetime.now().strftime("%d %B %Y")
    )

    # ==========================
    # Result Badge
    # ==========================

    if percentage >= 40:

        pdf.setFillColor(HexColor("#008000"))

        pdf.setFont("Helvetica-Bold", 20)

        pdf.drawString(
            width - 220,
            160,
            "PASS"
        )

    else:

        pdf.setFillColor(HexColor("#CC0000"))

        pdf.setFont("Helvetica-Bold", 20)

        pdf.drawString(
            width - 220,
            160,
            "FAIL"
        )

    # ==========================
    # Signature
    # ==========================

    pdf.setStrokeColor(HexColor("#000000"))

    pdf.line(
        width - 230,
        90,
        width - 80,
        90
    )

    pdf.setFillColor(HexColor("#000000"))

    pdf.setFont("Helvetica", 12)

    pdf.drawString(
        width - 205,
        70,
        "Authorized Signature"
    )

    # ==========================
    # Footer
    # ==========================

    pdf.setFillColor(HexColor("#666666"))
    pdf.setFont("Helvetica-Oblique", 10)

    pdf.drawCentredString(
        width / 2,
        40,
        "Generated by Online Examination Platform"
    )

    # ==========================
    # Save PDF
    # ==========================

    pdf.showPage()
    pdf.save()